function Lindblad_rhs!(du, ρ, p, t)
	@inbounds begin
		Ω1 = p[1]
		νp1 = p[2]
		Δ = p[3]
		Ω1ᶜ = conj(Ω1)
		Px1 = sine_wave(t, νp1, 4.71238898038469)
		Pz1 = sine_wave(t, νp1, 1.5707963267948966)
		norm1 = sqrt(Px1^2+Pz1^2)
		Px1 /= norm1
		Pz1 /= norm1
		du[1,1] = 7512198.44504092*ρ[3,3] - 1.0*1im*(427734.424303337*Ω1*ρ[3,1]*Px1 - 427734.424303337*Ω1ᶜ*ρ[1,3]*Px1)
		du[1,2] = -1.0*1im*(427734.424303337*Ω1*ρ[3,2]*Px1 - 248762.149579383*Ω1ᶜ*ρ[1,3]*Px1 - 1105530.87289429*ρ[1,2])
		du[1,3] = -5026548.24574367*ρ[1,3] - 1.0*1im*(-Δ*ρ[1,3] - 427734.424303337*Ω1*ρ[1,1]*Px1 - 248762.149579383*Ω1*ρ[1,2]*Px1 + 427734.424303337*Ω1*ρ[3,3]*Px1)
		du[2,1] = -1.0*1im*(248762.149579383*Ω1*ρ[3,1]*Px1 - 427734.424303337*Ω1ᶜ*ρ[2,3]*Px1 + 1105530.87289429*ρ[2,1])
		du[2,2] = 2540898.04644641*ρ[3,3] - 1.0*1im*(248762.149579383*Ω1*ρ[3,2]*Px1 - 248762.149579383*Ω1ᶜ*ρ[2,3]*Px1)
		du[2,3] = -5026548.24574367*ρ[2,3] - 1.0*1im*(-Δ*ρ[2,3] - 427734.424303337*Ω1*ρ[2,1]*Px1 - 248762.149579383*Ω1*ρ[2,2]*Px1 + 248762.149579383*Ω1*ρ[3,3]*Px1 + 1105530.87289429*ρ[2,3])
		du[3,1] = -5026548.24574367*ρ[3,1] - 1.0*1im*(Δ*ρ[3,1] + 427734.424303337*Ω1ᶜ*ρ[1,1]*Px1 + 248762.149579383*Ω1ᶜ*ρ[2,1]*Px1 - 427734.424303337*Ω1ᶜ*ρ[3,3]*Px1)
		du[3,2] = -5026548.24574367*ρ[3,2] - 1.0*1im*(Δ*ρ[3,2] + 427734.424303337*Ω1ᶜ*ρ[1,2]*Px1 + 248762.149579383*Ω1ᶜ*ρ[2,2]*Px1 - 248762.149579383*Ω1ᶜ*ρ[3,3]*Px1 - 1105530.87289429*ρ[3,2])
		du[3,3] = -10053096.4914873*ρ[3,3] - 1.0*1im*(-427734.424303337*Ω1*ρ[3,1]*Px1 - 248762.149579383*Ω1*ρ[3,2]*Px1 + 427734.424303337*Ω1ᶜ*ρ[1,3]*Px1 + 248762.149579383*Ω1ᶜ*ρ[2,3]*Px1)
	 end 
 	 nothing 
 end