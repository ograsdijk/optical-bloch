function Lindblad_rhs!(du, ρ, p, t)
	@inbounds begin
		Ω1 = p[1]
		νp1 = p[2]
		Δ = p[3]
		Ω1ᶜ = conj(Ω1)
		Px1 = sine_wave(t, νp1, 4.71238898038469)
		Pz1 = sine_wave(t, νp1, 1.5707963267948966)
		norm1 = sqrt(Px1^2+Pz1^2)
		Px1 /= norm1
		Pz1 /= norm1
		du[1,1] = 3351060.36142066*ρ[4,4] - 1.0*1im*(0.353554866850789*Ω1*ρ[4,1]*Px1 - 0.353554866850789*Ω1ᶜ*ρ[1,4]*Px1)
		du[1,2] = -1.0*1im*(0.353554866850789*Ω1*ρ[4,2]*Px1 + 0.5*Ω1ᶜ*ρ[1,4]*Pz1 - 0.178543090820313*ρ[1,2])
		du[1,3] = -1.0*1im*(0.353554866850789*Ω1*ρ[4,3]*Px1 + 0.353551915425323*Ω1ᶜ*ρ[1,4]*Px1 - 0.357666015625*ρ[1,3])
		du[1,4] = -5026548.24574367*ρ[1,4] - 1.0*1im*(-Δ*ρ[1,4] - 0.353554866850789*Ω1*ρ[1,1]*Px1 + 0.5*Ω1*ρ[1,2]*Pz1 + 0.353551915425323*Ω1*ρ[1,3]*Px1 + 0.353554866850789*Ω1*ρ[4,4]*Px1 - 0.178543090820313*ρ[1,4])
		du[2,1] = -1.0*1im*(-0.5*Ω1*ρ[4,1]*Pz1 - 0.353554866850789*Ω1ᶜ*ρ[2,4]*Px1 + 0.178543090820313*ρ[2,1])
		du[2,2] = 3351032.15690325*ρ[4,4] - 1.0*1im*(-0.5*Ω1*ρ[4,2]*Pz1 + 0.5*Ω1ᶜ*ρ[2,4]*Pz1)
		du[2,3] = -1.0*1im*(-0.5*Ω1*ρ[4,3]*Pz1 + 0.353551915425323*Ω1ᶜ*ρ[2,4]*Px1 - 0.179122924804688*ρ[2,3])
		du[2,4] = -5026548.24574367*ρ[2,4] - 1.0*1im*(-Δ*ρ[2,4] - 0.353554866850789*Ω1*ρ[2,1]*Px1 + 0.5*Ω1*ρ[2,2]*Pz1 + 0.353551915425323*Ω1*ρ[2,3]*Px1 - 0.5*Ω1*ρ[4,4]*Pz1)
		du[3,1] = -1.0*1im*(-0.353551915425323*Ω1*ρ[4,1]*Px1 - 0.353554866850789*Ω1ᶜ*ρ[3,4]*Px1 + 0.357666015625*ρ[3,1])
		du[3,2] = -1.0*1im*(-0.353551915425323*Ω1*ρ[4,2]*Px1 + 0.5*Ω1ᶜ*ρ[3,4]*Pz1 + 0.179122924804688*ρ[3,2])
		du[3,3] = 3351003.97316343*ρ[4,4] - 1.0*1im*(-0.353551915425323*Ω1*ρ[4,3]*Px1 + 0.353551915425323*Ω1ᶜ*ρ[3,4]*Px1)
		du[3,4] = -5026548.24574367*ρ[3,4] - 1.0*1im*(-Δ*ρ[3,4] - 0.353554866850789*Ω1*ρ[3,1]*Px1 + 0.5*Ω1*ρ[3,2]*Pz1 + 0.353551915425323*Ω1*ρ[3,3]*Px1 - 0.353551915425323*Ω1*ρ[4,4]*Px1 + 0.179122924804688*ρ[3,4])
		du[4,1] = -5026548.24574367*ρ[4,1] - 1.0*1im*(Δ*ρ[4,1] + 0.353554866850789*Ω1ᶜ*ρ[1,1]*Px1 - 0.5*Ω1ᶜ*ρ[2,1]*Pz1 - 0.353551915425323*Ω1ᶜ*ρ[3,1]*Px1 - 0.353554866850789*Ω1ᶜ*ρ[4,4]*Px1 + 0.178543090820313*ρ[4,1])
		du[4,2] = -5026548.24574367*ρ[4,2] - 1.0*1im*(Δ*ρ[4,2] + 0.353554866850789*Ω1ᶜ*ρ[1,2]*Px1 - 0.5*Ω1ᶜ*ρ[2,2]*Pz1 - 0.353551915425323*Ω1ᶜ*ρ[3,2]*Px1 + 0.5*Ω1ᶜ*ρ[4,4]*Pz1)
		du[4,3] = -5026548.24574367*ρ[4,3] - 1.0*1im*(Δ*ρ[4,3] + 0.353554866850789*Ω1ᶜ*ρ[1,3]*Px1 - 0.5*Ω1ᶜ*ρ[2,3]*Pz1 - 0.353551915425323*Ω1ᶜ*ρ[3,3]*Px1 + 0.353551915425323*Ω1ᶜ*ρ[4,4]*Px1 - 0.179122924804688*ρ[4,3])
		du[4,4] = -10053096.4914873*ρ[4,4] - 1.0*1im*(-0.353554866850789*Ω1*ρ[4,1]*Px1 + 0.5*Ω1*ρ[4,2]*Pz1 + 0.353551915425323*Ω1*ρ[4,3]*Px1 + 0.353554866850789*Ω1ᶜ*ρ[1,4]*Px1 - 0.5*Ω1ᶜ*ρ[2,4]*Pz1 - 0.353551915425323*Ω1ᶜ*ρ[3,4]*Px1)
	 end 
 	 nothing 
 end